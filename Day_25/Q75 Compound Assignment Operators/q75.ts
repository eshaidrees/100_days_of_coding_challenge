let x = 4;

// Addition
x += 2; // Equal to x = x + 2
console.log("Addition:", x );  // 6

// Subtraction
x -= 1; // Equal to x = x - 1
console.log("Subtraction:", x );  // 5

// Multiplication
x *= 3; // Equal to x = x * 3
console.log("Multiplication:", x ); // 15

// Division
x /= 2; // Equal to x = x / 2
console.log("Division:", x ); // 7.5

